//노드 생성
Node* createNode(Node NewData)
{
	Node* NewNode = (Node*)malloc(sizeof(Node));

	// 데이터 저장
	NewNode->num = NewData.num;  
	NewNode->classroom = NewData.classroom;
	NewNode->eng = NewData.eng;
	NewNode->math= NewData.math;
	strcpy(NewNode->Name,NewData.Name);
	//NewNode->NextNode = NewData.NextNode;
	NewNode->num = NewData.num;
	NewNode->ranks = NewData.ranks;
	NewNode->sci = NewData.sci;
	NewNode->total = NewData.total;





	NewNode->NextNode = NULL; //  다음 노드에 대한 포인터는 NULL로 초기화 한다. 

	return NewNode;//  노드의 주소를 반환한다.
}

//  노드 삭제
void destroyNode(Node* Node)
{
	free(Node);
}

//  노드 추가
void appendNode(Node** Head, Node* NewNode)
{
	/*  헤드 노드가 NULL이라면 새로운 노드가 Head */
	if ((*Head) == NULL)
	{
		*Head = NewNode;
	}
	else
	{
		/*  테일을 찾아 NewNode를 연결한다. */
		Node* Tail = (*Head);
		while (Tail->NextNode != NULL)
		{
			Tail = Tail->NextNode;
		}

		Tail->NextNode = NewNode;
	}
}

/*  노드 삽입 */
void insertAfter(Node* Current, Node* NewNode)
{
	NewNode->NextNode = Current->NextNode;
	Current->NextNode = NewNode;
}

void  insertNewHead(Node** Head, Node* NewHead)
{
	if (Head == NULL)
	{
		(*Head) = NewHead;
	}
	else
	{
		NewHead->NextNode = (*Head);
		(*Head) = NewHead;
	}
}

/*  노드 제거 */
void removeNode(Node** Head, Node* Remove)
{
	if (*Head == Remove)
	{
		*Head = Remove->NextNode;
	}
	else
	{
		Node* Current = *Head;
		while (Current != NULL && Current->NextNode != Remove)
		{
			Current = Current->NextNode;
		}

		if (Current != NULL)
			Current->NextNode = Remove->NextNode;
	}
}

/*  노드 탐색 */
Node* getNodeAt(Node* Head, int Location)
{
	Node* Current = Head;

	while (Current != NULL && (--Location) >= 0)
	{
		Current = Current->NextNode;
	}

	return Current;
}

/*  노드 수 세기 */
int getNodeCount(Node* Head)
{
	int   Count = 0;
	Node* Current = Head;

	while (Current != NULL)
	{
		Current = Current->NextNode;
		Count++;
	}

	return Count;
}
