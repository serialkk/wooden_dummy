bool	Sample::Render() {

	if (I_Input.KeyCheck(DIK_F4))
	{
		ApplyDSS(g_pImmediateContext, KDxState::g_pDepthDisable);
	}

	D3D11_VIEWPORT vp[4];
	vp[0].Width = (FLOAT)m_dwWidth;
	vp[0].Height = (FLOAT)m_dwHeight;
	vp[0].MinDepth = 0.0f;
	vp[0].MaxDepth = 1.0f;
	vp[0].TopLeftX = 0;
	vp[0].TopLeftY = 0;
	vp[1].Width = (FLOAT)m_dwWidth / 2;
	vp[1].Height = (FLOAT)m_dwHeight / 2;
	vp[1].MinDepth = 0.0f;
	vp[1].MaxDepth = 1.0f;
	vp[1].TopLeftX = m_dwWidth / 2;
	vp[1].TopLeftY = 0;

	vp[3].Width = (FLOAT)m_dwWidth / 2;
	vp[3].Height = (FLOAT)m_dwHeight / 2;
	vp[3].MinDepth = 0.0f;
	vp[3].MaxDepth = 1.0f;
	vp[3].TopLeftX = 0;
	vp[3].TopLeftY = m_dwHeight / 2;

	vp[2].Width = (FLOAT)m_dwWidth / 2;
	vp[2].Height = (FLOAT)m_dwHeight / 2;
	vp[2].MinDepth = 0.0f;
	vp[2].MaxDepth = 1.0f;
	vp[2].TopLeftX = m_dwWidth / 2;
	vp[2].TopLeftY = m_dwHeight / 2;

	g_pImmediateContext->RSSetViewports(4, vp);

	D3DXMatrixTranspose(&m_cbData.matView, &m_matView);
	D3DXMatrixTranspose(&m_cbData.matProj, &m_matProj);

	for (int iVp = 0; iVp < 3; iVp++)
	{
		m_cbData.vp = 0;

		//m_matWorld[iVp]._43 = iVp * 3.0f;
		D3DXMatrixTranspose(&m_cbData.matWorld, &m_matWorld[iVp]);
		g_pImmediateContext->UpdateSubresource(
			m_Object.g_pConstantBuffer, 0, NULL, &m_cbData, 0, 0);

		ApplyRS(g_pImmediateContext, KDxState::g_pCullSolidRS[m_Object.m_iCullMode]);

		if (m_Object.m_bWireFrameRender)
		{
			ApplyRS(g_pImmediateContext, KDxState::g_pWireFrameRS);
		}
		//      .
		g_pImmediateContext->IASetPrimitiveTopology(
			D3D_PRIMITIVE_TOPOLOGY_TRIANGLELIST);
		g_pImmediateContext->IASetInputLayout(m_Object.g_pInputlayout);
		UINT stride = sizeof(PNCT_VERTEX);
		UINT offset = 0;
		//if (iVp == 0)
		{
			g_pImmediateContext->IASetVertexBuffers(0,
				1, &m_Object.g_pVertexBuffer, &stride, &offset);
		}

		/*if (iVp == 1)
		{
			g_pImmediateContext->IASetVertexBuffers(0,
				1, &m_pVBSide, &stride, &offset);
		}*/
		
		g_pImmediateContext->IASetIndexBuffer(m_Object.g_pIndexBuffer, DXGI_FORMAT_R32_UINT, 0);
		g_pImmediateContext->VSSetConstantBuffers(0, 1, &m_Object.g_pConstantBuffer);
		g_pImmediateContext->VSSetShader(m_Object.g_pVertexShader, NULL, 0);
		g_pImmediateContext->GSSetConstantBuffers(0, 1, &m_Object.g_pConstantBuffer);
		g_pImmediateContext->GSSetShader(m_Object.g_pGeometryShader, NULL, 0);
		g_pImmediateContext->PSSetShader(m_Object.g_pPixelShader, NULL, 0);
		g_pImmediateContext->PSSetShaderResources(0, 1, &m_pTexSRV[iVp]);
		g_pImmediateContext->PSSetSamplers(0, 1, &m_pSamplerState);
		g_pImmediateContext->DrawIndexed(m_iNumIndex, 0, 0);
	}
	return true;
}
